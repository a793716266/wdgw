package com.flc.service.system.curriculuminfo.impl;

import java.util.List;
import javax.annotation.Resource;
import org.springframework.stereotype.Service;
import com.flc.dao.DaoSupport;
import com.flc.entity.Page;
import com.flc.util.PageData;
import com.flc.service.system.curriculuminfo.CurriculumInfoManager;

/** 
 * 说明： 课程详情表
 * 创建人：FLC
 * 创建时间：2018-07-24
 * @version
 */
@Service("curriculuminfoService")
public class CurriculumInfoService implements CurriculumInfoManager{

	@Resource(name = "daoSupport")
	private DaoSupport dao;
	
	/**新增
	 * @param pd
	 * @throws Exception
	 */
	public void save(PageData pd)throws Exception{
		dao.save("CurriculumInfoMapper.save", pd);
	}
	
	/**删除
	 * @param pd
	 * @throws Exception
	 */
	public void delete(PageData pd)throws Exception{
		dao.delete("CurriculumInfoMapper.delete", pd);
	}
	
	/**修改
	 * @param pd
	 * @throws Exception
	 */
	public void edit(PageData pd)throws Exception{
		dao.update("CurriculumInfoMapper.edit", pd);
	}
	
	/**列表
	 * @param page
	 * @throws Exception
	 */
	@SuppressWarnings("unchecked")
	public List<PageData> list(Page page)throws Exception{
		return (List<PageData>)dao.findForList("CurriculumInfoMapper.datalistPage", page);
	}
	
	/**列表(全部)
	 * @param pd
	 * @throws Exception
	 */
	@SuppressWarnings("unchecked")
	public List<PageData> listAll(PageData pd)throws Exception{
		return (List<PageData>)dao.findForList("CurriculumInfoMapper.listAll", pd);
	}
	
	/**通过id获取数据
	 * @param pd
	 * @throws Exception
	 */
	public PageData findById(PageData pd)throws Exception{
		return (PageData)dao.findForObject("CurriculumInfoMapper.findById", pd);
	}
	
	/**批量删除
	 * @param ArrayDATA_IDS
	 * @throws Exception
	 */
	public void deleteAll(String[] ArrayDATA_IDS)throws Exception{
		dao.delete("CurriculumInfoMapper.deleteAll", ArrayDATA_IDS);
	}
	/**批量下载
	 * @param ArrayDATA_IDS
	 * @throws Exception
	 */
	@Override
	public List<PageData> toBatchExcel(String[] ARRAYDATA_IDS) throws Exception {
		return (List<PageData>)dao.findForList("CurriculumInfoMapper.toBatchExcel", ARRAYDATA_IDS);
	}
	
}

